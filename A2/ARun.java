public class ARun {
    private int start;
    private int length;
    public ARun(int s, int l) {
        start = s;
        length = l;
    }
    public void setStart(int s){
        start = s;
    }
    public void setLength(int l){
        length = l;
    }
    public int getStart(){
        return start;
    }
    public int getLength(){
        return length;
    }
}
